#include "libft.h"

void	ft_strrev(char *s)
{
	int			i;
	int			i_end;
	char		c;

	i = 0;
	i_end = ft_strlen(s) - 1;
	while (i < i_end)
	{
		c = s[i];
		s[i++] = s[i_end];
		s[i_end--] = c;
	}
}
